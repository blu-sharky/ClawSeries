    let _locale = 'zh-CN';
    const _callbacks = [];

    function _detectLocale() {
        const stored = localStorage.getItem('clawseries-locale');
        if (stored && _dicts[stored]) return stored;
        const nav = navigator.language || '';
        return nav.startsWith('zh') ? 'zh-CN' : 'en';
    }

    function t(key, params) {
        const dict = _dicts[_locale] || _dicts['zh-CN'];
        let val = dict[key] || _dicts['zh-CN'][key] || key;
        if (params) {
            for (const [k, v] of Object.entries(params)) {
                val = val.replace(new RegExp('\\{' + k + '\\}', 'g'), v);
            }
        }
        return val;
    }

    function setLocale(locale) {
        if (!_dicts[locale]) return;
        _locale = locale;
        localStorage.setItem('clawseries-locale', locale);
        document.documentElement.lang = locale === 'zh-CN' ? 'zh-CN' : 'en';
    }

    function getLocale() { return _locale; }

    function applyHtml() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const text = t(key);
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = text;
            } else {
                el.textContent = text;
            }
        });
    }

    function onLocaleChange(cb) {
        _callbacks.push(cb);
    }

    function _fireChange() {
        applyHtml();
        _callbacks.forEach(cb => cb(_locale));
    }

    // Initialize locale
    _locale = _detectLocale();
    document.documentElement.lang = _locale === 'zh-CN' ? 'zh-CN' : 'en';

    return { t, setLocale, getLocale, applyHtml, onLocaleChange, _fireChange };
